package Model;

public class Calculadora {
	
	
	
	public float Adicao(float Primeiro,float Segundo)
	{
		float resultado = Primeiro + Segundo;
		return resultado;
	}
	public float Subtracao(float Primeiro,float Segundo)
	{
		float resultado = Primeiro - Segundo;
		return resultado;
	}
	public float Multiplicacao(float Primeiro,float Segundo)
	{
		float resultado = Primeiro * Segundo;
		return resultado;
	}
	public float Divisao(float Primeiro,float Segundo)
	{
		float resultado = Primeiro / Segundo;
		return resultado;
	}

}
